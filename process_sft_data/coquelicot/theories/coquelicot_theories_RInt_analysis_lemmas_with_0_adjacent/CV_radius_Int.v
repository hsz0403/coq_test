Lemma CV_radius_Int (a : nat -> R) : CV_radius (PS_Int a) = CV_radius a.
Proof.
rewrite -CV_radius_derive.
apply CV_radius_ext.
rewrite /PS_derive /PS_Int => n ; rewrite S_INR.
field.
apply Rgt_not_eq, INRp1_pos.
